/// Monoisotopic mass delta (Da) of a UNIMOD modification by id, or `None` if not tabulated.
pub fn mass_delta(id: u32) -> Option<f64> {
    UNIMOD_MASS
        .binary_search_by_key(&id, |&(i, _)| i)
        .ok()
        .map(|idx| UNIMOD_MASS[idx].1)
}

#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn is_sorted_and_looks_up() {
        assert!(UNIMOD_MASS.windows(2).all(|w| w[0].0 < w[1].0), "must be sorted, unique ids");
        assert!((mass_delta(21).unwrap() - 79.96633).abs() < 1e-3);
        assert!((mass_delta(4).unwrap() - 57.02146).abs() < 1e-3);
        assert_eq!(mass_delta(9_999_999), None);
    }

    #[test]
    fn builtin_catalog_agrees_with_the_full_table() {
        for m in crate::modification::BUILTIN {
            if let Some(mass) = mass_delta(m.unimod_id) {
                assert!((mass - m.mass_delta).abs() < 1e-3,
                    "{} (id {}): builtin {} vs table {}", m.name, m.unimod_id, m.mass_delta, mass);
            }
        }
    }
}
